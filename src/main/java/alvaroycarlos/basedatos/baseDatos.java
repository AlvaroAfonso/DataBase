    public String conectarBD(String usuario, String contraseña){
        //Si la String devuelta está vacía significará que fue todo correcto,
        //si devuelve algo, será un error
        String error = "";
        try{
            //System.out.println("Usuario: " + usuario + " y contraseña: " + String.valueOf(contraseña));
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://i7-lab5.dis.ulpgc.es/DIU_BD?useSSL=true",usuario,String.valueOf(contraseña));
            //System.out.println("Mensaje de conexion: " + con.toString());
            DatabaseMetaData md = con.getMetaData();
            String[] types = {"TABLE"};
            ResultSet rs = md.getTables(null, null, "%", types);
            while (rs.next()) {
                String tabla = rs.getString("TABLE_NAME");
                if(seleccion == null){
                    modelo.addElement(tabla);
                }else{
                    //System.out.println("Tabla: " + tabla);
                    if(seleccion.contains(tabla)){
                        ResultSet rs2 = md.getColumns(null, null, tabla, null);
                        while (rs2.next()) {
                            String columna = rs2.getString("COLUMN_NAME");
                            modelo.addElement(tabla + "." + columna);
                            //System.out.println(" Campo: " + columna);
                        }
                    }
                }
                
            }
            con.close();
        }catch(Exception e){
            error = e.getMessage();
            System.out.println("Mensaje de error: "+ error);
        }
        return error;
    }
    
