    private void conectarseBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_conectarseBotonActionPerformed
        usuario = usuarioCampo.getText();
        contraseña = contraseñaCampo.getPassword();
        if(usuario.equals("") || contraseña.equals("")){
            //MOSTRAR ERROR
            JOptionPane.showMessageDialog(this, "Ninguno de los campos puede quedar vacío", "Error en los datos", JOptionPane.ERROR_MESSAGE);  
            ocultarInterfaz();
            return;
        }
        String error = conectarBD(usuario, String.valueOf(contraseña));
        
        if(error.equals("")){
            //Conexion establecida
            conexionEstado.setText("CONECTADO");
            verInfoBoton.setVisible(true);
            jScrollPane1.setVisible(true);
            jLabel6.setVisible(true);
            simpleBoton.setVisible(true);
            intervaloBoton.setVisible(true);
            multipleBoton.setVisible(true);
            verInfoBoton.setVisible(true);
            limpiarBoton.setVisible(true);
            tablasBoton.setVisible(true);
            jLabel4.setVisible(true);
        }else{
            //mostrar error de conexion
            JOptionPane.showMessageDialog(this, "Error en la conexión a la base de datos", "Fallo de conexión", JOptionPane.ERROR_MESSAGE);  
            ocultarInterfaz();
        }
    }//GEN-LAST:event_conectarseBotonActionPerformed

